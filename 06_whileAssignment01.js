console.log(`3) WAP to Find First 15 Odd Numbers`);

let k = 1;  // initialization

while (k <= 15) {  // condition

    if (k % 2 != 0) {

        console.log(k);

    }

    k++; // update expression

}
console.log(`--------------------------------------------------------------`);

console.log(`4) WAP to Find First 15 Even Numbers`);

let p = 1;
while (p <= 15) {  // condition

    if (p % 2 == 0) {

        console.log(p);

    }

    p++; // update expression

}
console.log(`--------------------------------------------------------------`);

